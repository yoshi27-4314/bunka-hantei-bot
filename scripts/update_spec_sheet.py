def main():
    parser = argparse.ArgumentParser(description="システム仕様書スプレッドシートを更新")
    parser.add_argument("--dry-run", action="store_true", help="送信せず内容を表示")
    parser.add_argument("--sheet", help="指定シートのみ更新")
    args = parser.parse_args()

    targets = {args.sheet: SHEETS[args.sheet]} if args.sheet else SHEETS

    for name, data in targets.items():
        if args.dry_run:
            print(f"\n=== {name} ===")
            print(f"  ヘッダー: {data['headers']}")
            print(f"  行数: {len(data['rows'])}")
            for i, row in enumerate(data['rows'][:3]):
                print(f"  行{i+1}: {row}")
            if len(data['rows']) > 3:
                print(f"  ... 他 {len(data['rows'])-3} 行")
        else:
            payload = {
                "action": "update_spec_sheet",
                "sheet_name": name,
                "headers": data["headers"],
                "rows": data["rows"],
            }
            try:
                result = send_to_gas(payload)
                status = "OK" if result.get("ok") else f"FAIL: {result.get('error')}"
                print(f"  [{name}] {len(data['rows'])}行 ... {status}")
            except Exception as e:
                print(f"  [{name}] ERROR: {e}")

    if args.dry_run:
        print(f"\n[dry-run] {len(targets)} シート表示（送信なし）")
    else:
        print(f"\n[完了] {len(targets)} シート更新")


if __name__ == "__main__":
    main()
